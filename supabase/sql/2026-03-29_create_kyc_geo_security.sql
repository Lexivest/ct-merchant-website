on public.kyc_gps_audits
for select
to authenticated
using (
  merchant_id = auth.uid()
  or get_admin_role() is not null
);

drop policy if exists "Admin manage KYC GPS audits" on public.kyc_gps_audits;
create policy "Admin manage KYC GPS audits"
on public.kyc_gps_audits
for all
to authenticated
using (get_admin_role() is not null)
with check (get_admin_role() is not null);

