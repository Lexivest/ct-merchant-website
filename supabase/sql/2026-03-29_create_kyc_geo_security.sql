create extension if not exists postgis;

create table if not exists public.kyc_state_boundaries (
  id bigint generated by default as identity primary key,
  state_name text not null unique,
  state_code text,
  source_name text,
  source_boundary_id text,
  boundary geometry(MultiPolygon, 4326) not null,
  is_active boolean not null default true,
  created_at timestamp with time zone not null default timezone('utc'::text, now()),
  updated_at timestamp with time zone not null default timezone('utc'::text, now())
);

create index if not exists kyc_state_boundaries_active_idx
  on public.kyc_state_boundaries (is_active, state_name);

create index if not exists kyc_state_boundaries_boundary_gix
  on public.kyc_state_boundaries
  using gist (boundary);

alter table public.kyc_state_boundaries enable row level security;

drop policy if exists "Public read KYC state boundaries" on public.kyc_state_boundaries;
create policy "Public read KYC state boundaries"
on public.kyc_state_boundaries
for select
to public
using (is_active = true);

drop policy if exists "Admin manage KYC state boundaries" on public.kyc_state_boundaries;
create policy "Admin manage KYC state boundaries"
on public.kyc_state_boundaries
for all
to authenticated
using (get_admin_role() is not null)
with check (get_admin_role() is not null);

create table if not exists public.kyc_gps_audits (
  id bigint generated by default as identity primary key,
  shop_id bigint not null references public.shops(id) on delete cascade,
  merchant_id uuid not null references public.profiles(id) on delete cascade,
  expected_state_name text,
  matched_state_name text,
  gps_point geography(Point, 4326) not null,
  latitude double precision not null,
  longitude double precision not null,
  inside_expected_state boolean not null default false,
  nearest_state_name text,
  nearest_state_distance_km numeric(10, 3),
  boundary_source text,
  captured_at timestamp with time zone not null default timezone('utc'::text, now()),
  created_at timestamp with time zone not null default timezone('utc'::text, now())
);

create index if not exists kyc_gps_audits_shop_id_idx
  on public.kyc_gps_audits (shop_id, captured_at desc);

create index if not exists kyc_gps_audits_merchant_id_idx
  on public.kyc_gps_audits (merchant_id, captured_at desc);

create index if not exists kyc_gps_audits_point_gix
  on public.kyc_gps_audits
  using gist (gps_point);

alter table public.kyc_gps_audits enable row level security;

drop policy if exists "Merchant insert own KYC GPS audit" on public.kyc_gps_audits;
create policy "Merchant insert own KYC GPS audit"
on public.kyc_gps_audits
for insert
to authenticated
with check (merchant_id = auth.uid());

drop policy if exists "Merchant view own KYC GPS audit" on public.kyc_gps_audits;
