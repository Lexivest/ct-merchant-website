create or replace function public.record_kyc_gps_audit(
  p_shop_id bigint,
  p_merchant_id uuid,
  p_latitude double precision,
  p_longitude double precision
)
returns public.kyc_gps_audits
language plpgsql
security definer
set search_path = public
as $$
declare
  v_expected_state text;
  v_point geography(Point, 4326);
  v_inside public.kyc_state_boundaries%rowtype;
  v_nearest public.kyc_state_boundaries%rowtype;
  v_distance_km numeric(10, 3);
  v_inserted public.kyc_gps_audits%rowtype;
begin
  if auth.uid() is distinct from p_merchant_id and get_admin_role() is null then
    raise exception 'Not allowed to record this GPS audit.';
  end if;

  select c.state
  into v_expected_state
  from public.shops s
  join public.cities c on c.id = s.city_id
  where s.id = p_shop_id
    and s.owner_id = p_merchant_id
  limit 1;

  if v_expected_state is null then
    raise exception 'Expected state could not be resolved for this shop.';
  end if;

  v_point := st_setsrid(st_makepoint(p_longitude, p_latitude), 4326)::geography;

  select *
  into v_inside
  from public.kyc_state_boundaries sb
  where sb.is_active = true
    and st_intersects(sb.boundary, st_setsrid(st_makepoint(p_longitude, p_latitude), 4326))
  limit 1;

  select *
  into v_nearest
  from public.kyc_state_boundaries sb
  where sb.is_active = true
  order by st_distance(sb.boundary::geography, v_point)
  limit 1;

  if v_nearest.id is not null then
    v_distance_km := round((st_distance(v_nearest.boundary::geography, v_point) / 1000.0)::numeric, 3);
  end if;

  insert into public.kyc_gps_audits (
    shop_id,
    merchant_id,
    expected_state_name,
    matched_state_name,
    gps_point,
    latitude,
    longitude,
    inside_expected_state,
    nearest_state_name,
    nearest_state_distance_km,
    boundary_source
  )
  values (
    p_shop_id,
    p_merchant_id,
    v_expected_state,
    v_inside.state_name,
    v_point,
    p_latitude,
    p_longitude,
    coalesce(v_inside.state_name = v_expected_state, false),
    coalesce(v_inside.state_name, v_nearest.state_name),
    v_distance_km,
    coalesce(v_inside.source_name, v_nearest.source_name)
  )
  returning *
  into v_inserted;

  return v_inserted;
end;
$$;

grant execute on function public.record_kyc_gps_audit(bigint, uuid, double precision, double precision) to authenticated;

comment on table public.kyc_state_boundaries is
